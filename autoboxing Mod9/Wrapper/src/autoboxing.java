public class autoboxing {
    public static void main(String[] args) {
        
        //boxing
        double z = 73.12;
        Double zd = (z);
        System.out.println(zd);

        //unboxing
        Double z2 = 98.53;
        double zx = (z2);
        System.out.println(zx);
        
    }
 }
